package aula3a;

public class Cadeira {

    public String cor;
    public int qtd_pernas;
    private double valor;

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public int getQtd_pernas() {
        return qtd_pernas;
    }

    public void setQtd_pernas(int qtd_pernas) {
        this.qtd_pernas = qtd_pernas;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }
}
